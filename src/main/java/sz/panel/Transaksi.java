    private void loadProduk() {
    DefaultTableModel model = (DefaultTableModel) tblProduk.getModel();
    model.setRowCount(0);

    try {
        Connection con = Koneksi.Go();
        String sql = "SELECT id_produk, nama_produk, ukuran, harga, stok FROM produk";
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            Object[] row = {
                rs.getInt("id_produk"),
                rs.getString("nama_produk"),
                rs.getString("ukuran"),
                rs.getDouble("harga"),
                rs.getInt("stok")
            };
            model.addRow(row);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
  }

private void hitungTotal() {
    double total = 0;
    DefaultTableModel model = (DefaultTableModel) tblKeranjang.getModel();

    for (int i = 0; i < model.getRowCount(); i++) {
        String subtotalStr = model.getValueAt(i, 4).toString();
        subtotalStr = subtotalStr.replace(".", "")
                                 .replace(",", "");
        total += Double.parseDouble(subtotalStr);
    }

    lblTotal.setText("Total : Rp " + formatRupiah(total));
    lblTotal.revalidate();
    lblTotal.repaint();
}
